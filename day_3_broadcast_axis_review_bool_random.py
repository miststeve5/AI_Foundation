# --- 阶段 4：布尔索引 (Boolean Indexing) ---
# 核心思想：用条件表达式生成 mask，再用 mask 直接筛选/修改

# 1D 例子：传感器数据
data = np.array([1.5, -0.2, 5.5, 0.8, 12.0, -1.0])
print(f"\n原始传感器数据: {data}")

# 生成掩码：True 表示满足条件
mask = data > 0
print(f"大于 0 的掩码矩阵: {mask}")

# 过滤：只保留满足条件的元素
positive_data = data[data > 0]
print(f"只保留正数: {positive_data}")

# 就地修改：把所有负数直接归零
# 这种写法是向量化操作，避免 for 循环

data[data < 0] = 0
print(f"噪声归零后的数据: {data}")

# 多条件组合要加括号，且用 & / | (不能用 and / or)
filtered = data[(data > 1) & (data < 10)]
print(f"符合 1 < x < 10 条件的数据: {filtered}")


# 2D 例子：矩阵过滤
# 结果会展平成一维，因为筛选返回的是所有满足条件的元素

data_2d = np.array([[1, 6, 3], [8, 2, 9]])
mask_2d = data_2d > 5
print(f"\n2D 掩码矩阵:\n{mask_2d}")

filtered_2d = data_2d[data_2d > 5]
print(f"大于 5 的元素: {filtered_2d}")

# 修改：小于 5 的元素清零（常用于噪声剔除）
clean_data = data_2d.copy()
clean_data[clean_data < 5] = 0
print(f"小于 5 的元素清零后:\n{clean_data}")


