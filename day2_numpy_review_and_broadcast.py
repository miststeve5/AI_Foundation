#==深度拔高3：单位阵构建、高级索引与向量化点名法则==
N = 4
# --- 阶段 1：传统的逻辑循环 (基础版) ---
# 原理：显式地告诉计算机：“在行 i 列 i 的位置把 1 放进去”。
# 缺点：Python 层面的循环，处理大数据时性能较差。
my_eye = np.zeros((N, N))
for i in range(N):
    my_eye[i, i] = 1
print("阶段 1 - 循环生成的单位阵：\n", my_eye)


# --- 阶段 2：NumPy 高级索引/花式索引 (核心进阶) ---
# 原理：利用 np.arange(N) 生成一组【坐标名单】。
# 每一个 row_coords 里的数，都会与 col_coords 里对应位置的数配对成一个坐标。
# 配对过程：(row[0], col[0]), (row[1], col[1]), ...
# 这种机制叫“向量化点名”，它一次性在内存中定位所有坐标。

my_eye_fast = np.zeros((N, N))
row_coords = np.arange(N) # [0, 1, 2, 3]
col_coords = np.arange(N) # [0, 1, 2, 3]

# NumPy 底层会遍历这两张“名单”，直接对对应的坐标进行赋值
my_eye_fast[row_coords, col_coords] = 1 
print("\n阶段 2 - 高级索引生成的单位阵：\n", my_eye_fast)


# --- 阶段 3：进阶挑战 - 副对角线 ---
# 解析：
# 1. 行索引从 0 到 3，我们依然用 np.arange(N)。
# 2. 列索引需要从 3 到 0，我们利用 np.arange 的步长参数：
#    np.arange(起始值, 终止值, 步长) -> np.arange(N-1, -1, -1)
#    生成名单：[3, 2, 1, 0]
# 3. 每一行与对应的列索引配对：(0,3), (1,2), (2,1), (3,0)，正好覆盖副对角线。

my_anti_eye = np.zeros((N, N))
my_anti_eye[np.arange(N), np.arange(N-1, -1, -1)] = 1
print("\n阶段 3 - 副对角线：\n", my_anti_eye)


# --- 阶段 4：终极挑战 - 边缘赋值 ---
# 解析：
# 1. my_matrix[N-1, :] = 2
#    - N-1 是固定的行号（最后一行）。
#    - : 是切片操作，代表所有列。
# 2. my_matrix[:, N-1] = 2
#    - : 代表所有行。
#    - N-1 是固定的列号（最后一列）。
# 这不需要循环，NumPy 的广播机制会自动将数值 2 填充到选定的所有位置。

my_matrix = np.zeros((N, N))
my_matrix[N-1, :] = 2
my_matrix[:, N-1] = 2
print("\n阶段 4 - 最后一行与最后一列填 2：\n", my_matrix)


